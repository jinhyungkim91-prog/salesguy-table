// ── 적용 ───────────────────────────────────────────────────────────────────
const data = JSON.parse(fs.readFileSync(DATA_PATH, 'utf8'));
let awardCount = 0, storyCount = 0;

const updated = data.map(r => {
  if (AWARDS[r.id]) {
    r.award = AWARDS[r.id];
    awardCount++;
  }
  if (STORIES[r.id]) {
    r.story = STORIES[r.id];
    storyCount++;
  }
  return r;
});

fs.writeFileSync(DATA_PATH, JSON.stringify(updated, null, 2), 'utf8');

